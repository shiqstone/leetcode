package main

import "testing"

func Test_isValidSudoku(t *testing.T) {
	type args struct {
		board [][]byte
	}
	tests := []struct {
		name string
		args args
		want bool
	}{
		{
			name: "test1",
			args: args{
				board: [][]byte{
					[]byte{'5', '3', '.', '.', '7', '.', '.', '.', '.'},
					[]byte{'6', '.', '.', '1', '9', '5', '.', '.', '.'},
					[]byte{'.', '9', '8', '.', '.', '.', '.', '6', '.'},
					[]byte{'8', '.', '.', '.', '6', '.', '.', '.', '3'},
					[]byte{'4', '.', '.', '8', '.', '3', '.', '.', '1'},
					[]byte{'7', '.', '.', '.', '2', '.', '.', '.', '6'},
					[]byte{'.', '6', '.', '.', '.', '.', '2', '8', '.'},
					[]byte{'.', '.', '.', '4', '1', '9', '.', '.', '5'},
					[]byte{'.', '.', '.', '.', '8', '.', '.', '7', '9'},
				},
			},
			want: true,
		},
		{
			name: "test2",
			args: args{
				board: [][]byte{
					[]byte{'8', '3', '.', '.', '7', '.', '.', '.', '.'},
					[]byte{'8', '3', '.', '.', '7', '.', '.', '.', '.'},
					[]byte{'6', '.', '.', '1', '9', '5', '.', '.', '.'},
					[]byte{'.', '9', '8', '.', '.', '.', '.', '6', '.'},
					[]byte{'8', '.', '.', '.', '6', '.', '.', '.', '3'},
					[]byte{'4', '.', '.', '8', '.', '3', '.', '.', '1'},
					[]byte{'7', '.', '.', '.', '2', '.', '.', '.', '6'},
					[]byte{'.', '6', '.', '.', '.', '.', '2', '8', '.'},
					[]byte{'.', '.', '.', '4', '1', '9', '.', '.', '5'},
					[]byte{'.', '.', '.', '.', '8', '.', '.', '7', '9'},
				},
			},
			want: false,
		},
		{
			name: "test3",
			args: args{
				board: [][]byte{
					[]byte{'5', '3', '.', '.', '7', '.', '.', '.', '5'},
					[]byte{'6', '.', '.', '1', '9', '5', '.', '.', '.'},
					[]byte{'.', '9', '8', '.', '.', '.', '.', '6', '.'},
					[]byte{'8', '.', '.', '.', '6', '.', '.', '.', '3'},
					[]byte{'4', '.', '.', '8', '.', '3', '.', '.', '1'},
					[]byte{'7', '.', '.', '.', '2', '.', '.', '.', '6'},
					[]byte{'.', '6', '.', '.', '.', '.', '2', '8', '.'},
					[]byte{'.', '.', '.', '4', '1', '9', '.', '.', '5'},
					[]byte{'.', '.', '.', '.', '8', '.', '.', '7', '9'},
				},
			},
			want: false,
		},
		{
			name: "test4",
			args: args{
				board: [][]byte{
					[]byte{'5', '3', '.', '.', '7', '.', '.', '.', '.'},
					[]byte{'6', '.', '.', '1', '9', '5', '.', '.', '.'},
					[]byte{'.', '9', '5', '.', '.', '.', '.', '6', '.'},
					[]byte{'8', '.', '.', '.', '6', '.', '.', '.', '3'},
					[]byte{'4', '.', '.', '8', '.', '3', '.', '.', '1'},
					[]byte{'7', '.', '.', '.', '2', '.', '.', '.', '6'},
					[]byte{'.', '6', '.', '.', '.', '.', '2', '8', '.'},
					[]byte{'.', '.', '.', '4', '1', '9', '.', '.', '5'},
					[]byte{'.', '.', '.', '.', '8', '.', '.', '7', '9'},
				},
			},
			want: false,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := isValidSudoku(tt.args.board); got != tt.want {
				t.Errorf("isValidSudoku() = %v, want %v", got, tt.want)
			}
		})
	}
}
